private void updateRemainingCharacters() {  
  
    this.jLabelRemaining.setText(this.getRemainingCharacters() + " characters remaining");
}
private int getRemainingCharacters() {  
      System.out.println("Updateing " + this.maximum);
      System.out.println("  " + this.jTextArea1.getText().length());
    return (this.maximum) - this.jTextArea1.getText().length();
}
