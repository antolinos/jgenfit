public void setMaximumCharacters(int maximum) {
    this.maximum = maximum;
    this.jLabelMaximum.setText(this.maximum + " characters allowed");
    this.updateRemainingCharacters();
}

public void setText(String text) {
    this.oldText = text;
   this.jTextArea1.setText(text);
    this.updateRemainingCharacters();
}
    
